        value: {},
    }),
    created() {
        this.value.id = 0;
        this.value.userId = 0;
        this.value.createdAt = '2025-06-27';
    },
    watch: {
    },
    methods: {
        trackView() {
            this.$emit('trackView', this.value);
        },
        close() {
            this.$emit('closeDialog');
        },
        change() {
            this.$emit("update:modelValue", this.value);
        },
    }
}
</script>

